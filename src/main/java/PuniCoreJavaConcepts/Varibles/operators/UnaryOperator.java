package PuniCoreJavaConcepts.Varibles.operators;

public class UnaryOperator
{

public static void main(String[] args) 
{
    int a = 5;
    System.out.println("Initial a = " + a);
    System.out.println("++a: " + (++a));
    System.out.println("--a: " + (--a));
    System.out.println("-a: " + (-a));
    boolean flag = true;
    System.out.println("!flag: " + (!flag));
}
}
