package PuniCoreJavaConcepts.Varibles.operators;

public class AssignmentOperators
{
public static void main(String[] args)
{
    int a = 10;
    a += 5;
    System.out.println("a after a += 5: " + a); // 15
    a *= 2;
    System.out.println("a after a *= 2: " + a); // 30
    a -= 4;
    System.out.println("a after a -= 4: " + a); // 26
}
}
